package com.commsen.liferay.builtwith.api;

public class SiteStorageException extends Exception {

	private static final long serialVersionUID = 9142229452650753861L;

	public SiteStorageException() {
		super();
	}

	public SiteStorageException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public SiteStorageException(String message, Throwable cause) {
		super(message, cause);
	}

	public SiteStorageException(String message) {
		super(message);
	}

	public SiteStorageException(Throwable cause) {
		super(cause);
	}

}
